package Practice;

public class StringToAscii {

	public static void main(String[] args) {
		
      String s="Sayali";
      char[] arr=s.toCharArray();
      for(int i=0;i<arr.length;i++)
      {
    	  System.out.print(arr[i]+" ");
    	  int num=arr[i];
    	  System.out.println(num);
      }
      
	}

}
